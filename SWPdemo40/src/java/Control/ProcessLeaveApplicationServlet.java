    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        String employeeID = request.getParameter("employeeID");
        String choice = request.getParameter("choice");
        String leaveReason = request.getParameter("leaveReason");
        String leaveShift = request.getParameter("leaveShift");
        ApplicationDAO dao = new ApplicationDAO();
        dao.ProcessLeaveApplication(choice, employeeID, leaveReason, leaveShift);
        if (dao != null) {
            request.setAttribute("PC", "Đơn đã được giải quyết!");
        }
        request.getRequestDispatcher("ShowLeaveApplicationServlet").forward(request, response);
    }

